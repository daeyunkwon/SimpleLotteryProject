import UIKit

extension UIColor {
    static func customYellow() -> UIColor {
        return UIColor(red: 253/255, green: 203/255, blue: 0, alpha: 1)
    }
    
    static func customBlue() -> UIColor {
        return UIColor(red: 106/255, green: 199/255, blue: 240/255, alpha: 1)
    }
    
    static func customRed() -> UIColor {
        return UIColor(red: 254/255, green: 113/255, blue: 114/255, alpha: 1)
    }
    
    static func customGray() -> UIColor {
        return UIColor(red: 168/255, green: 168/255, blue: 168/255, alpha: 1)
    }
}
